package main

import (
    "testing"
)

func TestSub(t *testing.T) {
    testCases := []struct {
        b1, b2, expected bearing
    }{
        {20, 45, 25},
        {-45, 45, 90},
        {-85, 90, 175},
        {-95, 90, -175},
        {-45, 125, 170},
        {-45, 145, 190},
        {29.4803, -88.6381, -118.1184},
        {-78.3251, -159.036, -80.7109},
        {-70099.74233810938, 29840.67437876723, -139.583283123393},
        {-165313.6666297357, 33693.9894517456, -72.343918518687},
        {1174.8380510598456, -154146.66490124757, 132.503926299328},
        {60175.77306795546, 42213.07192354373, 317.21245929767},
    }

    for _, tc := range testCases {
        result := tc.b2.Sub(tc.b1)
        if result != tc.expected {
            t.Errorf("Sub(%f, %f) = %f; expected %f", tc.b1, tc.b2, result, tc.expected)
        }
    }
}
